int		check_alpha(char c)
{
	return ((c >= 65 && c <= 97) || (c >= 97 && c <= 122)) ? (1) : (0);
}

int		ft_str_is_alpha(char *str)
{
	while (*str)
	{
		if (!(check_alpha(*str)))
			return (0);
		str++;
	}
	return (1);
}
