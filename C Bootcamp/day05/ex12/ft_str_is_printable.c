int		check_p(char c)
{
	return (c >= 32 && c <= 126) ? (1) : (0);
}

int		ft_str_is_printable(char *str)
{
	while (*str)
	{
		if (!(check_p(*str)))
			return (0);
		str++;
	}
	return (1);
}
