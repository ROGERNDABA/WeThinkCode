int		check_up(char c)
{
	return (c >= 65 && c <= 90) ? (1) : (0);
}

int		ft_str_is_uppercase(char *str)
{
	while (*str)
	{
		if (!(check_up(*str)))
			return (0);
		str++;
	}
	return (1);
}
