char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char	*tmp;

	tmp = dest;
	while (n--)
		if (*src == '\0')
			*tmp++ = 0;
		else
			*tmp++ = *src++;
	return (dest);
}
