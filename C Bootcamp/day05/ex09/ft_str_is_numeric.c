int		check_num(char c)
{
	return (c >= 48 && c <= 57) ? (1) : (0);
}

int		ft_str_is_numeric(char *str)
{
	while (*str)
	{
		if (!(check_num(*str)))
			return (0);
		str++;
	}
	return (1);
}
