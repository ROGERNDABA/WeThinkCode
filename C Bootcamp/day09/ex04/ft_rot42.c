int		is_alpha(char str)
{
	if ((str >= 65 && str <= 90) || (str >= 97 && str <= 122))
		return (1);
	return (0);
}

char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (is_alpha(str[i]) == 1)
		{
			if ((str[i] >= 'a' && str[i] <= 'j') ||
			(str[i] >= 'A' && str[i] <= 'J'))
				str[i] += 16;
			else
				str[i] -= 10;
		}
		i++;
	}
	return (str);
}
