int		ft_atoi(char *str)
{
	int k;
	int n;

	k = 0;
	while (*str < 33)
		str++;
	if (*str == '-')
		n = 1;
	while (*str == '+' || *str == '-')
		str++;
	while (*str <= '9' && *str >= '0')
	{
		k = (k << 3) + (k << 1) + (*str) - '0';
		str++;
	}
	return (n == 1) ? (-k) : (k);
}
