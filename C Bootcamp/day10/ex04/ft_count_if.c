int		ft_count_if(char **tab, int (*f)(char *))
{
	int i;
	int j;
	int k;

	i = 0;
	k = 0;
	while (tab[i])
	{
		j = 0;
		while (tab[i][j])
		{
			if (f(&tab[i][j]))
			{
				k++;
				break ;
			}
			j++;
		}
		i++;
	}
	return (k);
}
